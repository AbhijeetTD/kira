"""Remediation executor — runs the AI-generated kubectl command.

No hardcoded playbooks.  The command is generated by either:
  1. The Decision Engine (deterministic patterns with exact commands)
  2. The LLM Judge (dynamic analysis producing exact kubectl commands)

This module just validates the command is safe and executes it.
"""
from __future__ import annotations

import asyncio
import logging
import re
import shlex
import subprocess

import json


import time


from backend.config import settings
